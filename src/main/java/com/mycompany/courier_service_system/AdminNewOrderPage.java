    private void newodrid() {

        try {

            FileReader FR = new FileReader(new File("order.txt"));
            LineNumberReader LNR = new LineNumberReader(FR);

            int linecount = 0;

            while (LNR.readLine() != null) {
                linecount++;
            }

            FR.close();
            LNR.close();

            txtOID.setText("odr" + (Integer.toString(linecount)));

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "Error");

        }
    }

