    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        String oid = txtOID.getText().trim();
        String ocd = txtOCD.getText().trim();
        String odd = "-";
        String odt = "-";
        String os = "Pending";
        String or = "-";;
        String op = String.valueOf(txtOP.getSelectedItem());
        String oa = txtOA.getText().trim();
        String cid = String.valueOf(jComboBox1.getSelectedItem());
        String rid = "-";

        try {
            if (!oid.isEmpty() && !ocd.isEmpty() && !op.equals("--Select Priority --") && !oa.isEmpty()) {
                try ( PrintWriter writer = new PrintWriter(new BufferedWriter(new FileWriter("order.txt", true)))) {
                    writer.append(oid + ",");
                    writer.append(ocd + ",");
                    writer.append(odd + ",");
                    writer.append(odt + ",");
                    writer.append(os + ",");
                    writer.append(or + ",");
                    writer.append(op + ",");
                    writer.append(oa + ",");
                    writer.append(cid + ",");
                    writer.append(rid + "\n");
                    writer.close();

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Error");
                }

                AdminHomePage AHP = new AdminHomePage();
                AHP.setVisible(true);
                this.setVisible(false);

            } else {
                JOptionPane.showMessageDialog(null, "Plese input data");
            }
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this, "add fail");
        }

    }//GEN-LAST:event_btnsaveActionPerformed

